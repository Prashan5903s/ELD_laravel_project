    form,
    {
        fields: {
            'permission_name': {
                validators: {
                    notEmpty: {
                        message: 'Permission name is required'
                    }
                }
            },
            'module_id': {
                validators: {
                    notEmpty: {
                        message: 'Module is required'
                    }
                }
            },
        },

        plugins: {
            trigger: new FormValidation.plugins.Trigger(),
            bootstrap: new FormValidation.plugins.Bootstrap5({
                rowSelector: '.fv-row',
                eleInvalidClass: '',
                eleValidClass: ''
            })
        }
    }
);

// Submit button handler
